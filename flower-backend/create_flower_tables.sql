-- ── 꽃 도감 (농사로 API 데이터) ──────────────────────────────
CREATE TABLE IF NOT EXISTS flower_book (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    data_no          VARCHAR(20) UNIQUE,
    name             VARCHAR(100) NOT NULL,
    scientific_name  VARCHAR(200),
    bloom_month      INT,
    bloom_day        INT,
    flower_language  TEXT,
    description      TEXT,  -- 꽃 설명
    grow_tips        TEXT,  -- 꽃 키우는 방법
    image_url        VARCHAR(1024),
    category_id      BIGINT REFERENCES flower_categories(id),
    source           VARCHAR(20) NOT NULL DEFAULT 'NONGSARO', -- NONGSARO | WIKIPEDIA | MANUAL
    status           VARCHAR(20) NOT NULL DEFAULT 'COMPLETE', -- COMPLETE | AUTO | PENDING
    created_at       TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_flower_book_name       ON flower_book(name);
CREATE INDEX IF NOT EXISTS idx_flower_book_scientific ON flower_book(scientific_name);
CREATE INDEX IF NOT EXISTS idx_flower_book_category   ON flower_book(category_id);
CREATE INDEX IF NOT EXISTS idx_flower_book_month      ON flower_book(bloom_month);

-- ── 꽃 명소 (GPS 기반 게시글, 챗봇 FlowerTools 사용) ─────────
CREATE TABLE IF NOT EXISTS flowers (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name              VARCHAR(100) NOT NULL,
    species           VARCHAR(200) NOT NULL,
    address           VARCHAR(255) NOT NULL,
    lat               DOUBLE PRECISION NOT NULL,
    lng               DOUBLE PRECISION NOT NULL,
    bloom_start       VARCHAR(50),
    bloom_end         VARCHAR(50),
    description       VARCHAR(1000),
    thumbnail_url     VARCHAR(1024),
    status            VARCHAR(20) NOT NULL DEFAULT 'BLOOMING',
    approval_status   VARCHAR(20) NOT NULL DEFAULT 'APPROVED',
    verification_count INT NOT NULL DEFAULT 0,
    created_at        TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_flowers_approval ON flowers(approval_status);
CREATE INDEX IF NOT EXISTS idx_flowers_species  ON flowers(species);

-- ── AI 학명 → 카테고리 매핑 ──────────────────────────────────
CREATE TABLE IF NOT EXISTS flower_species_mapping (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ai_name      VARCHAR(200) NOT NULL UNIQUE,
    category_id  BIGINT REFERENCES flower_categories(id),
    created_at   TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_species_mapping_name ON flower_species_mapping(ai_name);

